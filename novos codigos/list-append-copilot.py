# Armazenando uma variável do tipo string
TEXTO = "Exemplo de texto para armazenar na lista."

# Criando uma lista que armazena várias strings
lista_strings = ["String 1", "String 2", "String 3"]

# Utilizando o método append para adicionar a variável TEXTO à lista
lista_strings.append(TEXTO)

# Imprimindo toda a lista
print(lista_strings)